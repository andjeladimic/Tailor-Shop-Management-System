    private void popuniKategorije() {
        try {
            ArrayList<KategorijaKlijenta> kategorije = ClientController.getInstance().vratiListuSvihKategorijaKlijenta();

            jComboBoxKategorija.removeAllItems();

            for (KategorijaKlijenta kategorija : kategorije) {
                jComboBoxKategorija.addItem(kategorija);
            }

        } catch (Exception ex) {
            Logger.getLogger(FormNoviKlijent.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
