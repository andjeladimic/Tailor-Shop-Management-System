    private void jButtonSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSacuvajActionPerformed
        try {
            String ime = jTextFieldIme.getText();
            String prezime = jTextFieldPrezime.getText();
            String email = jTextFieldEmail.getText();
            KategorijaKlijenta kategorija = (KategorijaKlijenta) jComboBoxKategorija.getSelectedItem();

            if (ime.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Neophodno je uneti ime.");
                return;
            }
            if (prezime.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Neophodno je uneti prezime.");
                return;
            }
            if (email.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Neophodno je uneti email.");
                return;
            }

            Klijent k = new Klijent(-1, ime, prezime, email, kategorija);
            ClientController.getInstance().ubaciKlijenta(k);

            JOptionPane.showMessageDialog(this, "Sistem je ubacio klijenta.");
            mf.popuniKlijente();
            this.dispose();

        } catch (Exception ex) {
            Logger.getLogger(FormNoviKlijent.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Sistem ne moze da ubaci klijenta.");
        }
    }//GEN-LAST:event_jButtonSacuvajActionPerformed

