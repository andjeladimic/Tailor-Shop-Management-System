    private void popuniKategorije() {

        ArrayList<KategorijaKlijenta> kategorije;
        try {
            kategorije = ClientController.getInstance().vratiListuSvihKategorijaKlijenta();

            jComboBoxKategorija.removeAllItems();

            for (KategorijaKlijenta kategorija : kategorije) {
                jComboBoxKategorija.addItem(kategorija);
            }
        } catch (Exception ex) {
            Logger.getLogger(FormDetaljiKlijenta.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
