    private void jButtonSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSacuvajActionPerformed
        try {
            k.setIme(jTextFieldIme.getText());
            k.setPrezime(jTextFieldPrezime.getText());
            k.setEmail(jTextFieldEmail.getText());
            k.setKategorijaKlijenta((KategorijaKlijenta) jComboBoxKategorija.getSelectedItem());

            ClientController.getInstance().promeniKlijenta(k);

            JOptionPane.showMessageDialog(this, "Sistem je zapamtio klijenta.");

            jTextFieldIme.setEnabled(false);
            jTextFieldPrezime.setEnabled(false);
            jTextFieldEmail.setEnabled(false);
            jComboBoxKategorija.setEnabled(false);
            jButtonSacuvaj.setEnabled(false);

            fpk.refreshTable();
        } catch (Exception ex) {
            Logger.getLogger(FormDetaljiKlijenta.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Sistem ne moze da zapamti klijenta.");
        }
    }//GEN-LAST:event_jButtonSacuvajActionPerformed

    private void jButtonObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonObrisiActionPerformed
        int potvrda = JOptionPane.showConfirmDialog(this,
                "Da li ste sigurni da zelite da obrisete klijenta?",
                "Potvrda brisanja",
                JOptionPane.YES_NO_OPTION);

        if (potvrda == JOptionPane.YES_OPTION) {
            try {
                ClientController.getInstance().obrisiKlijenta(k);
                JOptionPane.showMessageDialog(this, "Sistem je obrisao klijenta.");
                fpk.refreshTable();
                this.dispose();
            } catch (Exception ex) {
                Logger.getLogger(FormDetaljiKlijenta.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Sistem ne moze da obrise klijenta.");
            }
        }
    }//GEN-LAST:event_jButtonObrisiActionPerformed

