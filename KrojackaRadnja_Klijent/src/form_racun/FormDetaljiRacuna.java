    private void jButtonSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSacuvajActionPerformed
        try {
            r.setMestoIzdavanja(jTextFieldMesto.getText());
            r.setKlijent((Klijent) jComboBoxKlijent.getSelectedItem());
            r.setKrojacica((Krojacica) jComboBoxKrojacica.getSelectedItem());

            TableModelStavkeRacuna tm = (TableModelStavkeRacuna) jTableStavke.getModel();
            r.setStavkeRacuna(tm.getLista());
            r.setUkupanIznos(tm.vratiUkupanIznos());

            ClientController.getInstance().promeniRacun(r);

            jTextFieldUkupanIznos.setText(String.valueOf(r.getUkupanIznos()));
            JOptionPane.showMessageDialog(this, "Sistem je zapamtio racun.");
            fpr.refreshTable();
            jTextFieldMesto.setEnabled(false);
            jComboBoxKlijent.setEnabled(false);
            jComboBoxKrojacica.setEnabled(false);
            jButtonSacuvaj.setEnabled(false);
            jButtonIzmeni.setEnabled(true);
            jButtonObrisiStavku.setEnabled(false);
            jButtonIzmeniStavku.setEnabled(false);
            jButtonDodajStavku.setEnabled(false);

        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Sistem ne moze da zapamti racun.");
        }
    }//GEN-LAST:event_jButtonSacuvajActionPerformed

    private void jButtonIzmeniStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIzmeniStavkuActionPerformed
        int row = jTableStavke.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Morate odabrati stavku za izmenu.");
            return;
        }
        TableModelStavkeRacuna tm = (TableModelStavkeRacuna) jTableStavke.getModel();
        StavkaRacuna stavka = tm.getLista().get(row);

        FormIzmenaStavke fis = new FormIzmenaStavke(null, true, stavka);
        fis.setVisible(true);

        // stavka je izmenjena direktno u memoriji kroz referencu
        tm.fireTableDataChanged();
        jTextFieldUkupanIznos.setText(String.valueOf(tm.vratiUkupanIznos()));
    }//GEN-LAST:event_jButtonIzmeniStavkuActionPerformed

    private void jButtonObrisiStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonObrisiStavkuActionPerformed
        int row = jTableStavke.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Morate odabrati stavku za brisanje.");
            return;
        }
        TableModelStavkeRacuna tm = (TableModelStavkeRacuna) jTableStavke.getModel();
        tm.obrisiStavku(row);
        jTextFieldUkupanIznos.setText(String.valueOf(tm.vratiUkupanIznos()));
    }//GEN-LAST:event_jButtonObrisiStavkuActionPerformed

    private void jButtonDodajStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDodajStavkuActionPerformed
        FormDodavanjeStavke fds = new FormDodavanjeStavke(null, true);
        fds.setVisible(true);

        StavkaRacuna nova = fds.getNovaStavka();
        if (nova == null) {
            return; // korisnik zatvorio bez dodavanja
        }
        nova.setRacun(r);
        TableModelStavkeRacuna tm = (TableModelStavkeRacuna) jTableStavke.getModel();
        tm.dodajStavku(nova);
        jTextFieldUkupanIznos.setText(String.valueOf(tm.vratiUkupanIznos()));
    }//GEN-LAST:event_jButtonDodajStavkuActionPerformed

