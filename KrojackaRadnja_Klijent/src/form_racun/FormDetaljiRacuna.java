    private void popuniKrojacice() {
        try {
            ArrayList<Krojacica> krojacice = ClientController.getInstance().vratiListuSvihKrojacica();
            jComboBoxKrojacica.removeAllItems();
            for (Krojacica k : krojacice) {
                jComboBoxKrojacica.addItem(k);
            }
        } catch (Exception ex) {
            Logger.getLogger(FormDetaljiRacuna.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }

    private void popuniKlijente() {
        try {
            ArrayList<Klijent> klijenti = ClientController.getInstance().vratiListuSvihKlijenata();
            jComboBoxKlijent.removeAllItems();
            for (Klijent k : klijenti) {
                jComboBoxKlijent.addItem(k);
            }
        } catch (Exception ex) {
            Logger.getLogger(FormDetaljiRacuna.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }

