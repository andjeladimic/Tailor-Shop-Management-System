    private void jButtonDodajStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDodajStavkuActionPerformed
        if (jTextFieldKolicina.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate uneti kolicinu.");
            return;
        }

        KrojackaUsluga krojackaUsluga = (KrojackaUsluga) jComboBoxKrojackaUsluga.getSelectedItem();
        int kolicina = Integer.parseInt(jTextFieldKolicina.getText());

        if (kolicina <= 0) {
            JOptionPane.showMessageDialog(this, "Kolicina mora biti veca od 0.");
            return;
        }

        StavkaRacuna sr = new StavkaRacuna(null, -1, kolicina, krojackaUsluga.getCena(),
                (kolicina * krojackaUsluga.getCena()), krojackaUsluga);

        TableModelStavkeRacuna tm = (TableModelStavkeRacuna) jTableStavkeRacuna.getModel();

//        if (tm.postojiKrojackaUsluga(krojackaUsluga)) {
//            JOptionPane.showMessageDialog(this, "Odabrana usluga je vec dodata na racun");
//            return;
//        }
        tm.dodajStavku(sr);

        ukupanIznos = tm.vratiUkupanIznos();
        jTextFieldUkupanIznos.setText(String.valueOf(ukupanIznos));
        jTextFieldKolicina.setText("");
    }//GEN-LAST:event_jButtonDodajStavkuActionPerformed

